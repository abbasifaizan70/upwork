<p>Спасибо что интересуетесь нами! Если у вас мало времени, то можете кликнуть на <a href="/news/1976/kak_my_jeto_delaem_1show_kz_insaiderskii_vzglyad_na_kollektiv" target="_blank">эту ссылку, здесь</a> интересная статья о компании глазами инсайдера.</p>

<p>Ну а если у вас есть минут 10, то читайте ниже! И обязательно посмотрите ролик о пятилетии нашей компании. Мы отпраздновали юбилей 1show.kz 15 ноября 2017 года.</p>

<p><iframe allowfullscreen="" frameborder="0" height="360" src="www.youtube.com/embed/_Wh6vch-uyw" width="640"></iframe></p>

<p>Наша компания была основана 1 марта 2012 года, под словом &laquo;основание&raquo; мы понимаем официальный запуск нашего сайта, который на протяжении уже 5 лет остается плацдармом общей&nbsp;деятельности компании 1show.kz! Опыт нашей компании в ивент- индустрии насчитывает 15 лет рабочего стажа учредителей-шоуменов Азамата и Антона. Однако, минувшие года работы под флагом 1show.kz положили начало новым горизонтам и амбициозным проектам.</p>

<p>Компания 1show.kz является официальным партнером управления культуры акимата столицы - с 2007 года мы занимаемся организацией концертных мероприятий, выставок, конференций, фестивалей и спортивных состязаний. В тесном взаимодействии с управлением культуры, нашей компании довелось провести множество крупных ивентов. Серия концертов &laquo;Қайырлы Кеш, Астана!&raquo; с участием звезд Казахстана и России. Серия концертов <a href="https://www.1show.kz/news/2304/seriya_koncertov_jenergiya_muzyki_priurocheno_k_jekspo-2017">&laquo;Энергия музыки&raquo;</a> - это летняя череда дискотек под открытым небом, которые проходили на людных площадях Астаны. Мероприятия были приурочены к общему медиаплану проведения международной выставки &laquo;Expo 2017&raquo; и собирали до 4000 человек за одно мероприятие.</p>

<p><a class="fancybox" href="news_photos/cke/2_IMG_0074.jpg" rel="gallery"><img src="news_photos/cke/2_IMG_0074_small.jpg" /></a><a class="fancybox" href="news_photos/cke/4_IMG_0035.jpg" rel="gallery"><img src="news_photos/cke/4_IMG_0035_small.jpg" /></a></p>

<p>Компания 1show.kz - это также и организация частных мероприятий, и таковых у нас всегда много. К нам обращаются за организацией тематических свадеб, интересных юбилеев, красивых вечеринок. Мы проводим до 350 частных мероприятий в год. Большинство из них, конечно, в столице, но немалую часть торжеств у нас заказывают и в регионах. Так, мы активно работаем в городах Павлодар, Кокшетау, Караганда, Петропавловск, Костанай, Семей и других. Чаще всего выездные сценарии нашей компании готовятся под <a href="https://www.1show.kz/news/2436/kyz-yzatu-v-stile-premii-oskar">Ұзату Той</a>, <a href="https://www.1show.kz/news/2387/gotovim-veselyj-besik-toj">Қалжа той, Бесік той</a> и другие национальные торжества. В нашем штате работают опытные и поэтому всегда востребованные режиссеры, координаторы и сценаристы проектов. Рекомендуем ознакомиться с паспортами проектов нашей компании <a href="/portfolio_1show_kz" target="_blank">по этой ссылке</a>, в данном разделе представлены концерты, презентации, свадьбы, дни рождения и вообще весь спектр услуг, оказываемых 1show.kz.</p>

<p>Кроме организации ивентов, мы активно занимаемся созданием радиопередач. Накопленный за 13 лет радийный опыт даёт нам возможности для прогнозирования интереса нашего слушателя. Опираясь на данный анализ, мы выпускаем абсолютно точные и целенаправленные продукты. На сегодняшний день мы работаем в направлении трех проектов: утреннее шоу <a href="https://www.1show.kz/news/2263/utrennee_shou_qaiyrly_tancy">&laquo;Qайырлы Танцы&raquo;</a> на радио &laquo;Астана&raquo;, вечернее шоу &laquo;Лига Джентльменов&raquo; в эфире станции &laquo;ORDA FM&raquo;, а также <a href="
https://www.1show.kz/news/2318/vechernee_shou_azamata_i_antona_oni_zdes_oni_s_vami">вечернее шоу Азамата и Антона</a>, которое выходит в эфир по будням с 17 до 20 часов на 101,4 FM. Кроме этого, вы всегда можете скачать электронную версию нашего <a href="https://www.1show.kz/journal_1showkz">журнала</a>, который мы выпускаем с 2013 года и по сей день. Журнал печатается для клиентов нашей компании и работает по принципу &laquo;сайт с собой&raquo;.</p>

